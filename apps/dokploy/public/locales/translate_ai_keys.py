def load_json(path: Path) -> dict:
    """加载 JSON 文件"""
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)

def dump_json(path: Path, data: dict) -> None:
    """保存 JSON 文件"""
    formatted = json.dumps(data, ensure_ascii=False, indent=4)
    with path.open("w", encoding="utf-8") as f:
        f.write(formatted + "\n")

def main():
    """主函数：应用翻译"""
    base_dir = Path(__file__).parent
    
    for lang_code, translations in TRANSLATIONS.items():
        common_path = base_dir / lang_code / "common.json"
        if not common_path.exists():
            print(f"⚠️  {lang_code}: common.json not found, skip")
            continue
        
        data = load_json(common_path)
        updated = 0
        
        for key, value in translations.items():
            if key in data:
                # 强制更新所有在翻译字典中的键
                data[key] = value
                updated += 1
        
        if updated > 0:
            dump_json(common_path, data)
            print(f"✓ {lang_code}: 更新了 {updated} 个键")
        else:
            print(f"✓ {lang_code}: 无需更新")

if __name__ == "__main__":
    main()

